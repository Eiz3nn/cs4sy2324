package CS4TruthRevis;

public class Burger {
    String name;
    int noOfBun;
    int noOfPatty;
    int noOfCheese;
    int noOfLettuce;
    int noOfTomato;
    
    public Burger(String n, int b, int p, int c, int l, int t) {
        name = n;
        noOfBun = b;
        noOfPatty = p;
        noOfCheese = c;
        noOfLettuce = l;
        noOfTomato = t;
    }
}